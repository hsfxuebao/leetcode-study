class Solution {
        public int numIslands(char[][] grid) {

            if (grid == null) {
                return 0;
            }
            int res = 0;
            int row = grid.length;
            int col = grid[0].length;
            for (int i = 0; i < row; i++) {
                for (int j = 0; j < col; j++) {
                    if (grid[i][j] == '1') {
                        dfs(grid, i,j);
                        res++;
                    }
                }
            }
            return res;

        }

        private void dfs(char[][] grid, int i, int j) {

            if (i < 0 || i >= grid.length || j < 0 || j >= grid[0].length) {
                return;
            }
            if (grid[i][j] == '0') {
                return;
            }
            grid[i][j] = '0';

            // ä¸Šä¸‹å·¦å³
            dfs(grid, i+1, j);
            dfs(grid, i-1, j);
            dfs(grid, i, j+1);
            dfs(grid, i, j-1);




        }
    }
