/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public boolean isSymmetric(TreeNode root) {
        // é€’å½’
//        return isSymmetricRec(root, root);
        // è¿­ä»£
        return isSymmetricIter(root, root);
    }

    private boolean isSymmetricIter(TreeNode pNode, TreeNode qNode) {

        Queue<TreeNode> queue = new LinkedList<>();
        queue.offer(pNode);
        queue.offer(qNode);

        while (!queue.isEmpty()) {

            TreeNode p = queue.poll();
            TreeNode q = queue.poll();
            if (p == null && q == null) {
                continue;
            }
            if (p == null || q == null) {
                return false;
            }
            if (p.val != q.val) {
                return false;
            }

            // åŠ å…¥é˜Ÿåˆ—
            queue.offer(p.left);
            queue.offer(q.right);

            queue.offer(p.right);
            queue.offer(q.left);
        }
        return true;
    }

    private boolean isSymmetricRec(TreeNode p, TreeNode q) {

        if (p == null && q == null) {
            return true;
        }
        if (p == null || q == null) {
            return false;
        }

        return p.val == q.val
                && isSymmetricRec(p.left, q.right)
                && isSymmetricRec(p.right, q.left);
    }
}
