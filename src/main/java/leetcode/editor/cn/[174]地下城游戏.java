class Solution {
    public int calculateMinimumHP(int[][] dungeon) {

        int m = dungeon.length;
        int n = dungeon[0].length;
        // dp[i][j] ä»iï¼Œjä½ç½®åˆ°å³ä¸‹è§’æœ€å°çš„å€¼
        int[][] dp = new int[m][n];
        // base case æœ€åä¸€è¡Œï¼Œæœ€åä¸€åˆ—
        // å³ä¸‹è§’çš„å€¼
        int val = 1 - dungeon[m-1][n-1];
        dp[m-1][n-1] = val > 0 ? val : 1;
        for (int i = m-2; i >= 0; i--) {
            int res = dp[i+1][n-1] - dungeon[i][n-1];
            dp[i][n-1] = res > 0 ? res : 1;
        }
        for (int j = n-2; j >= 0; j--) {
            int res = dp[m-1][j+1] - dungeon[m-1][j];
            dp[m-1][j] = res > 0 ? res : 1;
        }

        for (int i = m-2; i >= 0; i--) {
            for (int j = n-2; j >= 0; j--) {
                int res = Math.min(dp[i+1][j], dp[i][j+1]) - dungeon[i][j];
                dp[i][j] = res > 0 ? res : 1;
            }
        }

        return dp[0][0];
    }
}
